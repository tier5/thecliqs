class Store_Form_Admin_Custom_Fields extends Fields_Form_Standard
{
  public $_error = array();

  protected $_name = 'fields';

  protected $_elementsBelongTo = 'fields';

  public function init()
  {
    // custom store fields
    if( !$this->_item ) {
      $product_item = new Store_Model_Product(array('table'=> Engine_Api::_()->getDbtable('products', 'store')));
      $this->setItem($product_item);
    }

    parent::init();

    $this->removeElement('submit');
  }

  public function loadDefaultDecorators()
  {
    if( $this->loadDefaultDecoratorsIsDisabled() )
    {
      return;
    }

    $decorators = $this->getDecorators();
    if( empty($decorators) )
    {
      $this
        ->addDecorator('FormElements')
        ; //->addDecorator($decorator);
    }
  }
}